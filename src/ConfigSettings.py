# Configuration Settings

NUM_COLUMNS = 3 # The number of vertical columns to display the undrafted pokemon list in.

DISPLAY_UNDRAFTED_POKEMON_TYPES = True # Display the types of pokemon in the undrafted pokemon list.

DISPLAY_UNDRAFTED_POKEMON_EVOLUTION_INFO = True # Display the maximum potential evolution of undrafted pokemon and whether they can mega (This does not affect sorting)

DEFAULT_COMMAND = "/draft" # If this is set to "", you will be prompted to enter a command when the script starts. The code does not validate this so make sure you enter a valid command here.

DEFAULT_POKEMON_TO_GENERATE_PER_PLAYER = 12 # Set this to -1 to have the script ask you how many pokemon you'd like to randomly generate.

CONFIRM_POKEMON_PICKS = True # Controls whether the script will ask you to confirm every time you choose a pokemon.